package Variaveis;

public class Variaveis {
    public static void main(String[] args) {

        // Variáveis - espacos de memoria alocado 
        var number1 = 200;
        var number2 = "200";
        var number3 = 2.22;
        var number4 = true;
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);

        // TIPOS DE DADOS
        // variáveis -> tipo: String (textos)
        // variáveis -> tipo: Int (Numeros)
        // variáveis -> tipo: Double (numeros fracionados)
        // variáveis -> tipo: Booleans (True or False)

        //DADOS PRIMITIVOS
        // variáveis -> tipo: Booleans (True or False)
        // variaveis -> tipo: Byte (armazenar um conjunto de 8 bit)
        // variaveis -> tipo: Char (caracter - apenas uma letra)
        // variaveis -> tipo: Short (int só q com menos espaço)
        // variáveis -> tipo: Int (Numeros)
        // variaveis -> tipo: Long (int so que com mais espaço)
        // variáveis -> tipo: Float (32 bit, menor que o double)
        // variáveis -> tipo: Double (64 bit)

        //NÃO PRIMITIVOS
        // variáveis -> tipo: String (textos)

        // DECLARANDO VARIAVEIS:
        // tipo nomeDaRef = conteudoDaRef
        String texto = "ola"; // Representacao de texto
        int number5 = 10; //Representacao de numero inteiro
        double number6 = 10.5; //Representacao de numero fracionario
        boolean verdade = true; //Representacao de boolean
        byte b = 127;
        char c = 'c';
        short number7 = -129;
        long number8 = 1230000909876656L;
        float number9 = 19.19f;



        System.out.println(texto);
        System.out.println(number5);
        System.out.println(number6);
        System.out.println(verdade);



    }
    
}
